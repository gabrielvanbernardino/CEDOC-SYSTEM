CREATE TABLE accident_cases (
    case_id VARCHAR NOT NULL,
    case_number VARCHAR,
    case_date VARCHAR,
    time_label VARCHAR,
    case_type VARCHAR NOT NULL,
    name_of_caller VARCHAR,
    contact_number VARCHAR,
    call_taker VARCHAR,
    concern VARCHAR,
    action_taken VARCHAR,
    location VARCHAR NOT NULL,
    barangay VARCHAR,
    street_address VARCHAR,
    patient_name VARCHAR,
    status VARCHAR NOT NULL,
    created_by_cedoc_id VARCHAR NOT NULL,
    created_by_name VARCHAR NOT NULL,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    CONSTRAINT pk_accident_cases PRIMARY KEY (case_id),
    CONSTRAINT fk_accident_cases_created_by FOREIGN KEY (created_by_cedoc_id) REFERENCES users(cedoc_id)
);

