-- CEDOC Accident Recording and Reporting System
-- Clean standard relational schema for documentation and database design.
-- Public business tables only: 12 tables from the official database.

CREATE TABLE users (
    user_id BIGINT NOT NULL,
    cedoc_id VARCHAR NOT NULL,
    username VARCHAR,
    password VARCHAR,
    first_name VARCHAR,
    middle_name VARCHAR,
    surname VARCHAR,
    cedoc_name VARCHAR,
    email_address VARCHAR,
    contacts VARCHAR,
    category VARCHAR,
    designation VARCHAR,
    role VARCHAR,
    account_type VARCHAR,
    responder_type VARCHAR,
    vehicle_type VARCHAR,
    status VARCHAR,
    is_logged_in BOOLEAN NOT NULL,
    session_state VARCHAR NOT NULL,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    CONSTRAINT pk_users PRIMARY KEY (user_id),
    CONSTRAINT uq_users_cedoc_id UNIQUE (cedoc_id)
);

CREATE TABLE user_time_render (
    cedoc_id VARCHAR NOT NULL,
    full_name VARCHAR,
    activity_date DATE,
    login_at TIMESTAMP,
    logout_at TIMESTAMP,
    render_seconds BIGINT NOT NULL,
    render_time VARCHAR NOT NULL,
    case_report_created INTEGER NOT NULL,
    active_state VARCHAR NOT NULL,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    CONSTRAINT pk_user_time_render PRIMARY KEY (cedoc_id),
    CONSTRAINT fk_user_time_render_user FOREIGN KEY (cedoc_id) REFERENCES users(cedoc_id)
);

CREATE TABLE cedoc_id_year_sequence (
    registration_year INTEGER NOT NULL,
    last_sequence INTEGER NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    CONSTRAINT pk_cedoc_id_year_sequence PRIMARY KEY (registration_year)
);

CREATE TABLE system_setting (
    setting_key VARCHAR NOT NULL,
    system_name VARCHAR NOT NULL,
    organization_name VARCHAR NOT NULL,
    contact_number VARCHAR NOT NULL,
    email_address VARCHAR NOT NULL,
    time_zone VARCHAR NOT NULL,
    theme_mode VARCHAR NOT NULL,
    accent_color VARCHAR NOT NULL,
    sidebar_style VARCHAR NOT NULL,
    log_style VARCHAR NOT NULL,
    cmd_theme VARCHAR NOT NULL,
    updated_by VARCHAR NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    CONSTRAINT pk_system_setting PRIMARY KEY (setting_key)
